package com.mvc.bean;

public class StockBean {

	private int id;
	
	private int countwarehouse;
	
	private int countshop;
	
	private int total;

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCountwarehouse() {
		return countwarehouse;
	}

	public void setCountwarehouse(int countwarehouse) {
		this.countwarehouse = countwarehouse;
	}

	public int getCountshop() {
		return countshop;
	}

	public void setCountshop(int countshop) {
		this.countshop = countshop;
	}
	
}
